public class Allocation {
	private double amount;

	/**
	 * @param amount
	 */
	public Allocation(double amount) {
		super();
		this.amount = amount;
	}

	/**
	 * @return the amount
	 */
	public double getAmount() {
		return amount;
	}

	/**
	 * @param amount the amount to set
	 */
	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	
}
